package Paquete;

public class Secuencia_Principal {

    public void a() {
        for (int a = 10; a <= 50; a += 10) {
            System.out.print(a + " - ");
        }

        for (int b = 70; b < 90; b += 10) {
            System.out.print(b + " - ");
        }

        System.out.print("100");
    }
}
